public class Task6_DataTypeDemo {
    public static void main(String[] args) {
        int age = 25;
        float height = 5.8f;
        double pi = 3.14159265359;
        char grade = 'A';
        boolean isJavaFun = true;

        System.out.println("Integer (int): " + age);
        System.out.println("Float (float): " + height);
        System.out.println("Double (double): " + pi);
        System.out.println("Character (char): " + grade);
        System.out.println("Boolean (boolean): " + isJavaFun);
    }
}
